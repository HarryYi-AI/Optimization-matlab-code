% 雅可比矩阵函数
function J = Jacob(x)
    global t1 t2
    J1 = x(3) * t1 ./ (1 + x(1) * t1 + x(2) * t2) - x(1) * x(3) * t1 .* (t1) ./ (1 + x(1) * t1 + x(2) * t2).^2;
    J2 = -x(1) * x(3) * t1 * t2 ./ (1 + x(1) * t1 + x(2) * t2) .^ 2;
    J3 = x(1) * t1 ./ (1 + x(1) * t1 + x(2) * t2) ;
    J = [J1; J2; J3];
end